<?php
    // 变量
    // php变量类型是一个弱类型语言，类型包括整型，浮点型，布尔，数组，对象，NULL，字符串
    // 整型
    $a = 10;
    echo $a;
    echo "<br>";
    // 浮点型
    $b = 10.33;
    echo $b;
    echo "<br>";
    // 布尔
    $c = false;
    echo $c;
    echo "<br>";
    // 字符串
    $d = "ooo";
    echo $d;
    echo "<br>";
    // 数组
    $e = ['hello'];
    // echo $e[0];
    print_r($e);
    echo "<br>";
    var_dump($e);

    
 ?>
